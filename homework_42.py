#Model (хранит данные)

class Film:
    def __init__(self, название, жанр, режиссер, год, длительность, студия, актеры):
        self.название = название
        self.жанр = жанр
        self.режиссер = режиссер
        self.год = год
        self.длительность = длительность
        self.студия = студия
        self.актеры = актеры


class FilmModel:
    def __init__(self):
        self.фильмы = []

    def добавить(self, фильм):
        self.фильмы.append(фильм)

    def получить_все(self):
        return self.фильмы

    def получить_по_номеру(self, номер):
        if 0 <= номер < len(self.фильмы):
            return self.фильмы[номер]
        return None

    def удалить(self, номер):
        if 0 <= номер < len(self.фильмы):
            self.фильмы.pop(номер)
            return True
        return False


#View (Отобажение данных)

class FilmView:
    def показать_меню(self):
        print("\nРедактирование данных каталога фильмов")
        print("Действия с фильмами:")
        print("1 - добавление фильма")
        print("2 - каталог фильмов")
        print("3 - просмотр определенного фильма")
        print("4 - удаление фильма")
        print("q - выход из программы")

    def запросить_данные_фильма(self):
        print("=" * 48)
        название = input("Введите название фильма: ")
        жанр = input("Введите жанр: ")
        режиссер = input("Введите режиссера: ")
        год = input("Введите год выпуска: ")
        длительность = input("Введите длительность (мин): ")
        студия = input("Введите студию: ")
        актеры = input("Введите актеров (через запятую): ")
        return Film(название, жанр, режиссер, год, длительность, студия, актеры)

    def показать_каталог(self, фильмы):
        print("=" * 48)
        if len(фильмы) == 0:
            print("Каталог пуст.")
        else:
            for i, ф in enumerate(фильмы):
                print(f"{i + 1}. {ф.название} ({ф.год}) - {ф.жанр}")

    def показать_фильм(self, фильм):
        print("=" * 48)
        print(f"Название:     {фильм.название}")
        print(f"Жанр:         {фильм.жанр}")
        print(f"Режиссер:     {фильм.режиссер}")
        print(f"Год выпуска:  {фильм.год}")
        print(f"Длительность: {фильм.длительность} мин")
        print(f"Студия:       {фильм.студия}")
        print(f"Актеры:       {фильм.актеры}")

    def запросить_номер(self, подсказка):
        try:
            номер = int(input(подсказка))
            return номер - 1
        except ValueError:
            return -1

    def показать_сообщение(self, текст):
        print(текст)


#Controller (управляет логикой)

class FilmController:
    def __init__(self):
        self.модель = FilmModel()
        self.вид = FilmView()

    def добавить_фильм(self):
        фильм = self.вид.запросить_данные_фильма()
        self.модель.добавить(фильм)
        self.вид.показать_сообщение("Фильм успешно добавлен!")

    def показать_каталог(self):
        фильмы = self.модель.получить_все()
        self.вид.показать_каталог(фильмы)

    def просмотреть_фильм(self):
        фильмы = self.модель.получить_все()
        self.вид.показать_каталог(фильмы)
        if len(фильмы) == 0:
            return
        номер = self.вид.запросить_номер("Введите номер фильма: ")
        фильм = self.модель.получить_по_номеру(номер)
        if фильм:
            self.вид.показать_фильм(фильм)
        else:
            self.вид.показать_сообщение("Фильм не найден.")

    def удалить_фильм(self):
        фильмы = self.модель.получить_все()
        self.вид.показать_каталог(фильмы)
        if len(фильмы) == 0:
            return
        номер = self.вид.запросить_номер("Введите номер фильма для удаления: ")
        if self.модель.удалить(номер):
            self.вид.показать_сообщение("Фильм удален.")
        else:
            self.вид.показать_сообщение("Фильм не найден.")

    def запустить(self):
        while True:
            self.вид.показать_меню()
            выбор = input("Выберите вариант действия: ")

            if выбор == "1":
                self.добавить_фильм()
            elif выбор == "2":
                self.показать_каталог()
            elif выбор == "3":
                self.просмотреть_фильм()
            elif выбор == "4":
                self.удалить_фильм()
            elif выбор == "q":
                print("Выход из программы.")
                break
            else:
                print("Неверный выбор. Попробуйте снова.")

if __name__ == "__main__":
    app = FilmController()
    app.запустить()
